const languageBtn = document.getElementById("language-toggle");

let language = localStorage.getItem("language") || "en";

changeLanguage(language);

languageBtn.onclick=()=>{

    language = language==="en" ? "ar":"en";

    localStorage.setItem("language",language);

    changeLanguage(language);

}

function changeLanguage(lang){

    document.body.dir=lang==="ar"?"rtl":"ltr";

    document.documentElement.lang=lang;

    languageBtn.innerHTML=lang==="ar"?" AR":" EN";

    document.querySelectorAll("[data-lang]").forEach(item=>{

        const key=item.dataset.lang;

        if(translations[lang][key]){

            item.textContent=translations[lang][key];

        }
document.querySelectorAll("[data-lang-placeholder]").forEach(item => {

    const key = item.dataset.langPlaceholder;

    if (translations[lang][key]) {

        item.placeholder = translations[lang][key];

    }

});
    });

}